// eslint-disable-next-line no-unused-vars
function areaSquare(side) {
    return side * side;
}

// eslint-disable-next-line no-unused-vars
function areaRectangle(length, width) {
    return length * width;
}

// eslint-disable-next-line no-unused-vars
function areaParallelogram(base, height) {
    return base * height;
}

// eslint-disable-next-line no-unused-vars
function areaRhombus(base, height) {
    return base * height;
}